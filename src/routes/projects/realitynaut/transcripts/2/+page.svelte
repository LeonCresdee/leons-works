	We’re looking for people who make playful things with technology. Tell us about something you’ve
	made, the kinds of things you’re interested in and any technical skills you might have
</h2>

<p>A bit of background on me:</p>

<p>
	I’ve been programming things since I was a teenager. I got my Bachelors in Computer science. After
	that I was hired at Aardman Animations as their Stop Motion Pipeline Developer. There I’ve been
	lucky enough to be a part of two feature films: Chicken Run 2 and the latest Wallace & Gromit
	(releasing later this year), along with a handful of other series, and shorts.
</p>

<p>
	Most of what I’ve created at Aardman is technology in support of creativity. Internal tools for
	tracking and processing shots, asset managers, etc. I do what I can to try and add bits of humour
	and playful things. But like many creative technologists in industry sometimes you have to do what
	is needed for the team.
</p>
<p>Unfortunately I can’t show you any of that work but here’s some things I can:</p>
<p>[Demo DeNode]</p>
<p>
	This is a project I did a while ago when I was interested in ciphers and encryption. I wanted a
	way to make cryptography more accessible to those who can’t program. I opted to make a visual
	scripting system aimed at students to use. It was written in Java using the Processing visual arts
	framework.
</p>
<p>[Demo dissertation]</p>
<p>
	This was a part of dissertation project. The project was about procedurally generating urban
	neighbourhoods that were less susceptible to crime. My main interest was in the procedural
	generation aspect which is an area I’m still really interested in.
</p>
<p>[Demo Urban Atlas]</p>
<p>
	Here’s a project I’m currently working on. It’s an interactive web map for people to find places
	to skateboard. It’s not just for finding skateparks, it allows for users to mark location of
	curbs, benches, and any other place they’ve found that inspires them to skate.
</p>
<p>
	I’m also currently trying to push myself into more hardware focused projects. I’ve been able to
	put together circuits like this effects pedal, not of my own design but a practice for assembling
